@Slf4j
@UsePapaSpider
public class BiYouXinSpider extends PapaSpider implements AjaxHook {

	private ChromeAjaxHookDriver chromeDriver;
	private boolean checkTel = false;
	private boolean vcodeSuc = false;//验证码是否正确

	@Override
	public String message() {
		return "碧有信金融（www.biyouxin.com）成立于2016年4月，注册资金5000万元，是国内著名房产企业——碧桂园集团旗下互联网金融平台。";
	}

	@Override
	public String platform() {
		return "biyouxin";
	}

	@Override
	public String home() {
		return "biyouxin.com";
	}

	@Override
	public String platformName() {
		return "碧有信金融";
	}

	@Override
	public String[] tags() {
		return new String[] {"P2P", "借贷" , "商业房贷"};
	}
	
	@Override
	public Set<String> getTestTelephones() {
		return Sets.newHashSet("15985268904", "18210538513");
	}

	private String getImgCode() {
		for (int i = 0 ; i < 3; i++) {
			try {
				WebElement img = chromeDriver.findElementByCssSelector("#valicodeImg");
				img.click();smartSleep(1000);
				byte[] body = chromeDriver.screenshot(img);
				return OCRDecode.decodeImageCode(body);
			} catch (Exception e) {
				e.printStackTrace();
			}
		}
		return "";
	}

	@Override
	public boolean checkTelephone(String account) {
		try {
			chromeDriver = ChromeAjaxHookDriver.newChromeInstance(true, true);
			chromeDriver.get("https://cms.biyouxinjr.com/userCenter/#/register");
			chromeDriver.addAjaxHook(this);
			chromeDriver.findElementByCssSelector("input[type='tel']").sendKeys(account);
			for (int i = 0; i < 5; i++) {
				chromeDriver.findElementByCssSelector("input[type='password']").click();
				smartSleep(3000);
				if (vcodeSuc) {
					break;
				}
			}
		} catch (Exception e) {
			e.printStackTrace();
		}finally {
			if (chromeDriver != null) {
				chromeDriver.quit();
			}
		}
		return checkTel;
	}

	@Override
	public boolean checkEmail(String account) {
		return false;
	}

	@Override
	public Map<String, String> getFields() {
		return null;
	}

	@Override
	public HookTracker getHookTracker() {
		return HookTracker.builder().addUrl("api/checkUserExist").build();
	}

	@Override
	public HttpResponse filterRequest(HttpRequest request, HttpMessageContents contents, HttpMessageInfo messageInfo) {
		return null;
	}

	@Override
	public void filterResponse(HttpResponse response, HttpMessageContents contents, HttpMessageInfo messageInfo) {
		vcodeSuc = true;
		JSONObject result = JSON.parseObject(contents.getTextContents());
		checkTel = result.getJSONObject("res_data").get("isExist").toString().equals("1");
	}

}
